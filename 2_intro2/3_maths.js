// Mathematical expressions
// +, -, *, / , % (modulo), ** (exponentiation)

// SUM
console.log( 1 + 1 ); // 2
console.log("1 + 100"); // "1 + 100"
console.log("1" + 100); // "1100"
console.log(1 + "100"); // "1100"

// SUBTRACTION
console.log( 100 - 50 ); // 50

// MULTIPLICATION * symbol
console.log( 3 * 3 ); // 9

// DIVISION / symbol
console.log( 6 / 3 ); // 2

console.clear();
// MODULO %
// Returns whatever is left after a division ( remainder )
console.log( 6 % 3 ); // 0 there is nothing left after the division
console.log( 10 % 3 ); // 1
console.log( 10 % 6 ); // 4

// EXPONENTIATION **
// 2 to the power of 3
console.log( 2 ** 3 ); // 2 * 2 * 2 = 8


// ***********************************************

let myVar = 2;
myVar = myVar + 5; // myVar = 2 + 5
console.log( myVar ); 7

// Without repeting ourselves
let myVarBis = 2;
myVarBis += 5; // myVarBis = myVarBis + 5
console.log( myVarBis ); // 7

// The = can be combined with other math symbols
/*
    https://www.w3schools.com/js/js_operators.asp

    Operator  | Example  | same as
    +=	      | x += y	 |  x = x + y
    -=	      | x -= y	 |  x = x - y
    *=	      | x *= y	 |  x = x * y
    /=	      | x /= y	 |  x = x / y
    %=	      | x %= y	 |  x = x % y
    **=	      | x **= y	 |  x = x ** y
*/

let myVar3 = 2;
myVar3 *= 3;
console.log( myVar3 ); // 6
